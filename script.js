// Dil değiştirme fonksiyonu
function changeLanguage(lang) {
    const elements = document.querySelectorAll('[data-lang]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Dil seçicilerini güncelle
    document.querySelectorAll('.language-selector a').forEach(link => {
        link.classList.remove('active');
    });
    
    // Aktif dil seçicisini işaretle
    const activeLink = document.querySelector(`.language-selector a[data-lang="${lang}"]`);
    if (activeLink) {
        activeLink.classList.add('active');
    }
    
    // Local storage'a kaydet
    localStorage.setItem('selectedLanguage', lang);
}

document.addEventListener('DOMContentLoaded', function() {
    // Scroll animasyonu için elementleri izle
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate-in');
            }
        });
    }, {
        threshold: 0.1
    });

    // Animasyon için elementleri seç
    document.querySelectorAll('.service-card, .result-card, .contact-card, .section-header, .about-content').forEach(element => {
        observer.observe(element);
    });

    // Dil seçenekleri arasında geçiş
    const languageLinks = document.querySelectorAll('.language-selector a');
    languageLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            
            const lang = this.getAttribute('data-lang');
            changeLanguage(lang);
        });
    });

    // Sayfa yüklendiğinde kaydedilmiş dili kullan
    const savedLang = localStorage.getItem('selectedLanguage') || 'tr';
    changeLanguage(savedLang);

    // Menü maddelerine tıklandığında kaydırma davranışı
    document.querySelectorAll('nav a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            
            const targetId = this.getAttribute('href');
            const targetElement = document.querySelector(targetId);
            
            if (targetElement) {
                const headerOffset = 160;
                const elementPosition = targetElement.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.pageYOffset - headerOffset;
                
                window.scrollTo({
                    top: offsetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });

    // Header artık sabit kalıyor - scroll davranışı kaldırıldı

        // Mobil menü toggle işlevi
    const mobileMenuBtn = document.querySelector('.mobile-menu-btn');
    const nav = document.querySelector('.main-nav');
    
    if (mobileMenuBtn && nav) {
        // Click event
        mobileMenuBtn.addEventListener('click', (e) => {
            e.preventDefault();
            e.stopPropagation();
            toggleMobileMenu();
        });
        
        // Touch event for mobile devices
        mobileMenuBtn.addEventListener('touchstart', (e) => {
            e.preventDefault();
            toggleMobileMenu();
        });
        
        // Keyboard support
        mobileMenuBtn.addEventListener('keydown', (e) => {
            if (e.key === 'Enter' || e.key === ' ') {
                e.preventDefault();
                toggleMobileMenu();
            }
        });
        
        function toggleMobileMenu() {
            nav.classList.toggle('mobile-menu-open');
            const icon = mobileMenuBtn.querySelector('i');
            
            if (nav.classList.contains('mobile-menu-open')) {
                icon.classList.remove('fa-bars');
                icon.classList.add('fa-times');
                mobileMenuBtn.setAttribute('aria-expanded', 'true');
                document.body.style.overflow = 'hidden'; // Prevent scrolling when menu is open
            } else {
                icon.classList.remove('fa-times');
                icon.classList.add('fa-bars');
                mobileMenuBtn.setAttribute('aria-expanded', 'false');
                document.body.style.overflow = ''; // Restore scrolling
            }
        }
        
        // Menü linklerine tıklandığında menüyü kapat
        nav.querySelectorAll('a').forEach(link => {
            link.addEventListener('click', () => {
                nav.classList.remove('mobile-menu-open');
                const icon = mobileMenuBtn.querySelector('i');
                icon.classList.remove('fa-times');
                icon.classList.add('fa-bars');
                mobileMenuBtn.setAttribute('aria-expanded', 'false');
                document.body.style.overflow = '';
            });
        });
        
        // Menü dışına tıklandığında menüyü kapat
        document.addEventListener('click', (e) => {
            if (!nav.contains(e.target) && !mobileMenuBtn.contains(e.target)) {
                if (nav.classList.contains('mobile-menu-open')) {
                    nav.classList.remove('mobile-menu-open');
                    const icon = mobileMenuBtn.querySelector('i');
                    icon.classList.remove('fa-times');
                    icon.classList.add('fa-bars');
                    mobileMenuBtn.setAttribute('aria-expanded', 'false');
                    document.body.style.overflow = '';
                }
            }
        });
    }
}); 